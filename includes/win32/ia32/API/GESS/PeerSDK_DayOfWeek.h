#pragma once

/**
* @brief Specifies the day of the week.
*/
enum DayOfWeek
{
    /**
    * Indicates Sunday.
    */
    DayOfWeek_Sunday = 0,

    /**
    * Indicates Monday.
    */
    DayOfWeek_Monday = 1,

    /**
    * Indicates Tuesday.
    */
    DayOfWeek_Tuesday = 2,

    /**
    * Indicates Wednesday.
    */
    DayOfWeek_Wednesday = 3,

    /**
    * Indicates Thursday.
    */
    DayOfWeek_Thursday = 4,

    /**
    * Indicates Friday.
    */
    DayOfWeek_Friday = 5,

    /**
    * Indicates Saturday.
    */
    DayOfWeek_Saturday = 6,

    /**
    * Indicates holiday.
    */
    DayOfWeek_Holiday = 7,
};
